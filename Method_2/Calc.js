//Addition

export const add = (a,b) => {
    let sum = a+b;
    return ("Addition Is : "+sum);
}


// Subtraction

export const sub = (a,b) => {
    let diff = a-b;
    return ("Subtraction Is : "+diff);
}


//Multiplication

export const mul = (a,b) => {
    let prod = a*b;
    return ("Multiplication Is : "+prod);
}

//Division

export const div = (a,b) => {
    let divide = a/b;
    return ("Division Is : "+divide);
}